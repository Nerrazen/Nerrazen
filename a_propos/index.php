                    <h2>Qui sui-je ?</h2>
                </div>
                <div class="text">
                    <p>Salut à tous et à toutes, je suis Nerrazen, l'auteur d'<strong><a href="../adens_core">Adens Core</a></strong>, de &nbsp<strong><a href="../real_games">Real Games</a></strong>&nbspet de&nbsp<strong><a href="../#">Magus</a></strong>. J'ai 19 ans et j'adore lire des mangas, mais surtout regarder des animés. Ma plus grande passion est le développement web (site codé par mes soins) et écrire des histoires. Si vous aimez lire mes histoires et que vous voulez le faire savoir, n'hésitez pas à aller sur ma page Facebook et sur wattpad pour me le faire savoir. POurquoi j'écris des histoires ? Depuis tout petit j'imagines beacoup d'histoire, je mets toujours en scène ce que j'image (ce qui peut être souvent ridicule). Mais comme on me le dit, le ridicule ne tue pas, donc j'avance et je continue à imaginer.</p>
                </div>
                <div class="title">
                    <h2>Où je puise mes idées ?</h2>
                </div>
                <div class="text">
                    <p>Bonne question ! Je puise principalement mes idées dans des mangas, des films, voir même dans la rue. Dans les mangas ? Principalement tout son contenu, personnages, structure, les dessins, l'amour, etc ... Un anime qui m'est resté sur le coeur est Zero no Tsukaima, mais il y en a bien d'autres. Dans les films ? Leur histoire, oui, leur contexte comme l'horreur, l'action, répliques, etc ... J'ai tiré mon idée de Real Games de The Walking Dead, mais aussi de World War Z, comme le dit un commando : "<i>La cervelle c'est nikel, les rotules c'est pas nul !</i>" Dans la rue ? Alors là, c'est la cerise sur le gateaux, quelques répliques sont reprises lors de mes sorties avec des amis. L'exemple parfait est quand je me suis rendu à un lac avec un ami, on y allait en vélo. Un moment sur la route, je lui demande par où est le lac et il me répondit : "<i>C'est à côté de quelque part par là bas.</i>"</p>
                    <p>Sinon les jeux de roles sont aussi une source d'inspiration, c'est en jouant à un jeu comme ça que j'ai put créer Real Games de fond en comble. On avait aussi fait un jeu médiévale-aventure mais c'est vite partit en cacahuète. Les jeux vidéos abrutissent ? Mensonges ! J'en suis la preuve (d'accord mauvais exemple)! Les jeux vidéos sont de bons moyens d'inspiration pour un monde médiévale-fantastique. Le bestiaire, les sorts, armes, etc ... </p>
                    <p>Les noms de personnages, pays, villes, etc ... sont aussi difficiles à trouver n'est-ce pas ? Pour trouver un nom de personnage j'utilise cette méthode :</br>
                    &nbsp&nbsp&nbsp&nbsp&nbsp1) Je tate des lettres et des suites de lettres afin de former un mot</br>
                    &nbsp&nbsp&nbsp&nbsp&nbsp2) Avec ce mot, j'essaie de l'améliorer pour en former un nom</br>
                    &nbsp&nbsp&nbsp&nbsp&nbsp3) Si ce mot me plaie je le garde, sinon Google Traduction et je me base sur une langue étrangère</br>
                    &nbsp&nbsp&nbsp&nbsp&nbsp4) Une fois traduit, ça donne un jolie mot qui peut être porté en nom, souvent ridicule</br></br>
                    Sinon je cherche sur internet des noms mythologiques comme pour Abigor dans l'histoire d'Adens Core. Pour les pays, je reprend souvent le nom du pays original et je le traduit en une autre langue. Je change biensûr quelques lettres dans le nom et pour les villes je fait pareil.</p>
                    <p>Un peu de mal au début, mais vous verez, ça viendra tout seul après ! Comme le dit un ami : "<i>C'est en forgeant qu'on devient forgeron !</i>"</p>
